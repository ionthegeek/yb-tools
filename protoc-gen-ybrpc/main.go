package main

import (
	"google.golang.org/protobuf/compiler/protogen"
)

func main() {
	protogen.Options{}.Run(func(gen *protogen.Plugin) error {
		for _, f := range gen.Files {
			if !f.Generate {
				continue
			}
			if f.Services != nil {
				_ = generateFile(gen, f)
			}
		}
		return nil
	})
}

// generateFile generates a _ascii.pb.go file containing gRPC service definitions.
func generateFile(gen *protogen.Plugin, file *protogen.File) *protogen.GeneratedFile {
	filename := file.GeneratedFilenamePrefix + "_ybrpc.pb.go"
	g := gen.NewGeneratedFile(filename, file.GoImportPath)
	g.P("// Code generated by protoc-gen-ybrpc. DO NOT EDIT.")
	g.P()
	g.P("package ", file.GoPackageName)
	g.P()

	g.P()

	for _, service := range file.Services {
		g.P("// service: " + service.Desc.FullName())
		g.P("// service: " + service.GoName)
		g.P()
		g.P("// methods")
		g.P("type ", service.GoName, "Impl struct {}")

		for _, method := range service.Methods {
			g.P("func (s *" + service.GoName + "Impl)" + method.GoName + "(ts string, pb *" + method.Input.GoIdent.GoName + ")" + "(*" + method.Output.GoIdent.GoName + ", error) {")
			//g.P("// - " + method.GoName)
			//g.P("//   - " + method.Input.GoIdent.GoName)
			//g.P("//   - " + method.Output.GoIdent.GoName)
			//g.P("//    - " + method.Desc.Name())
			g.P("    return &" + method.Output.GoIdent.GoName + "{}, nil")
			g.P("}")
			g.P("")
		}
	}
	g.P()

	return g
}
